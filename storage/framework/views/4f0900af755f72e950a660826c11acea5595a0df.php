        function updatePrices(month) {
            var basePrice, baseHour;

            if (enterprisePrice) {
                const newPrice = enterprisePrice.getAttribute(`data-price${activeCurrency}`);
                if (newPrice) {
                    enterprisePrice.textContent = newPrice;
                }
            }

            prices.forEach(function (price, index) {
                var initialPrice = parseInt(price.getAttribute("data-pricew2")) / 3;
                var initialHour = parseInt(priceHours[index].getAttribute("data-hourw2"));

                if (activeCurrency === "eur") {
