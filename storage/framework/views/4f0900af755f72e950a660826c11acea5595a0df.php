                    basePrice = parseInt(price.getAttribute("data-pricew2"));
                    baseHour = parseInt(priceHours[index].getAttribute("data-hourw2"));
                    allparent.classList.remove("show-txt");
                }

                if (month === 6) {
                    price.textContent = Math.round(basePrice * 2 * 0.9 / 6);
                    priceHours[index].textContent = Math.round(baseHour * 0.9);
                    itemparent.classList.add("saleactive");
                } else if (month === 12) {
                    price.textContent = Math.round(basePrice * 4 * 0.8 / 12);
                    priceHours[index].textContent = Math.round(baseHour * 0.8);
                    itemparent.classList.add("saleactive");
                } else {
                    price.textContent = basePrice / 3;
                    priceHours[index].textContent = baseHour;
                    itemparent.classList.remove("saleactive");
                }
            });

            updateCurrencyClass();

            updateOldPrices();
        }

        function updateOldPrices() {
            oldPrices.forEach(function (oldPrice) {
                var oldPriceValue;

                if (activeCurrency === "eur") {
                    oldPriceValue = oldPrice.getAttribute("data-oldeur");
                } else if (activeCurrency === "gbp") {
                    oldPriceValue = oldPrice.getAttribute("data-oldegbp");
                } else {
                    oldPriceValue = oldPrice.getAttribute("data-old");
                }

                oldPrice.textContent = oldPriceValue;
            });
        }

